/* S2MPU06_CHG_STATUS1 */
#define CHG_STATUS1_OTG_SS_ENB	0
#define CHG_STATUS1_CHGVIN_STS	2
#define CHG_STATUS1_BOOST_STS	3
#define	CHG_STATUS1_CHG_DONE	4
