#define CHG_STATUS1_VBAT_LVL	6

/* S2MPU06_CHG_STATUS2 */
#define DET_BAT_STATUS_SHIFT	3
#define DET_BAT_STATUS_MASK	BIT(DET_BAT_STATUS_SHIFT)
#define CHGVINOVP_STS_SHIFT	6

/* S2MPU06_CHG_CTRL1 */
#define EN_CHG_SHIFT		7
#define EN_CHG_MASK		BIT(EN_CHG_SHIFT)

