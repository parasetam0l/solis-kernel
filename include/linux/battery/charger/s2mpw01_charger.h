#define UART_BOOT_ON_STATUS_MASK        BIT(UART_BOOT_ON_STATUS_SHIFT)
#define TMROUT_STATUS_SHIFT     4
#define TMROUT_STATUS_MASK      BIT(TMROUT_STATUS_SHIFT)
#define CHGTSD_STATUS_SHIFT     5
#define CHGTSD_STATUS_MASK      BIT(CHGTSD_STATUS_SHIFT)
#define MRSTB_STATUS_SHIFT      6
#define MRSTB_STATUS_MASK       BIT(MRSTB_STATUS_SHIFT)
#define WDT_STATUS_SHIFT        7
#define WDT_STATUS_MASK BIT(WDT_STATUS_SHIFT)


/* S2MPW01_CHG_CTRL1 */
#define EN_CHG_SHIFT		7
#define EN_CHG_MASK		BIT(EN_CHG_SHIFT)

/* S2MPW01_CHG_CTRL2 */
#define FAST_CHARGING_CURRENT_SHIFT	1
