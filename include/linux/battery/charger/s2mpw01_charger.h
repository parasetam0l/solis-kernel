#define S2MPW01_CHG_REG_INT1		0x00
#define S2MPW01_CHG_REG_INT2		0x01
#define S2MPW01_CHG_REG_INT3		0x02
#define S2MPW01_CHG_REG_INT1M		0x03
#define S2MPW01_CHG_REG_INT2M		0x04
#define S2MPW01_CHG_REG_INT3M		0x05
