#define CIN2BAT_STATUS_MASK	BIT(CIN2BAT_STATUS_SHIFT)
#define CHGSTS_STATUS_SHIFT	4
#define CHGSTS_STATUS_MASK	BIT(CHGSTS_STATUS_SHIFT)
#define TOP_OFF_STATUS_SHIFT    2
#define TOP_OFFSTATUS_MASK      BIT(TOP_OFF_STATUS_SHIFT)

/* S2MPW01_CHG_STATUS2 */
#define UART_BOOT_OFF_STATUS_SHIFT      0
#define UART_BOOT_OFF_STATUS_MASK       BIT(UART_BOOT_OFF_STATUS_SHIFT)
#define USB_BOOT_ON_STATUS_SHIFT        1
#define USB_BOOT_ON_STATUS_MASK BIT(USB_BOOT_ON_STATUS_SHIFT)
#define USB_BOOT_OFF_STATUS_SHIFT       2
#define USB_BOOT_OFF_STATUS_MASK        BIT(USB_BOOT_OFF_STATUS_SHIFT)
#define JIGON_STATUS_SHIFT      3
#define JIGON_STATUS_MASK       BIT(JIGON_STATUS_SHIFT)
#define AD_PATH_STATUS_SHIFT    4
