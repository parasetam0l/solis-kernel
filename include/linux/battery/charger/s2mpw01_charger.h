#define CHGIN_INPUT_STATUS_MASK BIT(CHGIN_INPUT_STATUS_SHIFT)
#define DET_BAT_STATUS_SHIFT    7
#define DET_BAT_STATUS_MASK     BIT(DET_BAT_STATUS_SHIFT)

